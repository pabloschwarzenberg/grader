n=input()
w=0
while w<len(n):
    if n[w]=="T":
        print("Secuencia correcta")
    elif n[w]=="G":
        print("Secuencia correcta")
    elif n[w]=="C":
        print("Secuencia correcta")
    elif n[w]=="A":
        print("Secuencia correcta")
    else:
        print("Secuencia incorrecta")
    w=w+1