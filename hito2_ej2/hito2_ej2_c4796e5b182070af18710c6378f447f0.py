g=input("Ingrese la secuencia del genoma: ")
c=0
while c<len(g):
  if g[c]=="A":
    print("secuencia correcta")
  elif g[c]=="C":
    print("secuencia correcta")
  elif g[c]=="T":
    print("secuencia correcta")
  elif g[c]=="G":
    print("secuencia correcta")
  else:
    print("secuencia incorrecta")
  c=c+1

