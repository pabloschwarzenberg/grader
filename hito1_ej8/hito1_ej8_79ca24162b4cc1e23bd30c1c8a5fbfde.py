a=int(input())
if 1000<=a<=9999:
    print((a//1000),"M +",(((a//100)-(a//1000)*10)),"C +",(((a//10)-(a//100)*10)),"D +",((a-(a//10)*10)),"U")
elif 99<a<=999:
    print((a//100),"C +",(((a//10)-(a//100)*10)),"D +",(a-(a//10)*10),"U")
elif 10<=a<=99:
    print((a//10),"D +",(a-(a//10)*10),"U")
else:
    print(a,"U")