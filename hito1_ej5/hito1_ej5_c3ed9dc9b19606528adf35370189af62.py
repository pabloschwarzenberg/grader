#Cálculo del dígito verificador de un rut
rut=int(input())
a=rut//10000000
b=(rut-a*10000000)//1000000
c=(rut-((b*1000000)+(a*10000000)))//100000
d=(rut-((b*1000000)+(a*10000000)+(c*100000)))//10000
e=(rut-((b*1000000)+(a*10000000)+(c*100000)+(d*10000)))//1000
f=(rut-((b*1000000)+(a*10000000)+(c*100000)+(d*10000)+(e*1000)))//100
g=(rut-((b*1000000)+(a*10000000)+(c*100000)+(d*10000)+(e*1000)+(f*100)))//10
h=rut-((b*1000000)+(a*10000000)+(c*100000)+(d*10000)+(e*1000)+(f*100)+(g*10))
suma=h*2+g*3+f*4+e*5+d*6+c*7+b*2+a*3
division=suma%11
dv=11-division
if dv==11:
  print("dv=0")
else:
  if dv==10:
    print("dv=K")
  else:
    print("dv=",dv)