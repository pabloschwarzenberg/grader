def area_triangulo(cateto1,cateto2):
    resultado=((cateto1*cateto2)/2)
    return resultado

def area_rectangulo(base1,altura1):
    resultado=(base1*altura1)
    return resultado


def area_rombo(diagonal1,diagonal2):
    resultado=((diagonal1*diagonal2)/2)
    return resultado

def area_circulo(radio):
    import math
    resultado=(math.pi*(radio**2))
    return resultado

           