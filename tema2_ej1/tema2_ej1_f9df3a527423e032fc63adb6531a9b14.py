import math
def area_triangulo(base,altura):
    resultado1=(base*altura)/2
    return resultado1
def area_rectangulo(lado,ancho):
    resultado2=(lado*ancho)
    return resultado2
def area_rombo(diagonalD,diagonald):
    resultado3=(diagonalD*diagonald)/2
    return resultado3
def area_circulo(r):
    resultado4=math.pi*r*r
    return resultado4
