import math

def area_triangulo(base,altura):
    a1=(base*altura)/2
    return a1

def area_rectangulo(base,altura):
    a2=base*altura
    return a2

def area_rombo(diagonal1,diagonal2):
    a3=(diagonal1*diagonal2)/2
    return a3

def area_circulo(radio):
    a3=math.pi*radio*radio
    return a3

           