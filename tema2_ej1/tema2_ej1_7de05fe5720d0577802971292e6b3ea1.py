import math 

def area_triangulo(ba,al):
    area = ba*al/2
    return area
    pass

def area_rectangulo(ba,al):
    area = ba*al
    return area
    pass

def area_rombo(di1,di2):
    area = di1*di2/2
    return area
    pass

def area_circulo(r):
    area = float(math.pi*r*r)
    return area
    pass


           