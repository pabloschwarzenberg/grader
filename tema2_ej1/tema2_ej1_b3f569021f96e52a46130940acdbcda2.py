import math
def area_triangulo(base,altura):
 resultado=(base*altura)/2
 return resultado

def area_rectangulo(ancho,largo):
 resultado=(ancho*largo)
 return resultado

def area_rombo(Diagonal,diagonal):
    resultado=(Diagonal*diagonal)/2
    return resultado


def area_circulo(r):
    resultado=((math.pi)*r**2)
    return resultado
           