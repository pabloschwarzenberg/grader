from math import pi
def area_triangulo(base,altura):
    A_T=(base*altura)/2
    return A_T

def area_rectangulo(base,altura):
    A_Rec=base*altura
    return A_Rec

def area_rombo(diagonal1,diagonal2):
    A_Rom=(diagonal1*diagonal2)/2
    return A_Rom

def area_circulo(radio):
    A_C=pi*(radio**2)
    return A_C


         