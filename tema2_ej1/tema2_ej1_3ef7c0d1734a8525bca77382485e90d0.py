import math
def area_triangulo(base,altura):
  return (base*altura)/2
  
def area_rectangulo(base,altura):
  return (base*altura)
  
def area_rombo(diagonal_1,diagonal_2):
  return (diagonal_1*diagonal_2)/2

def area_circulo(r):
  return (math.pi*r*r)
  
