def area_triangulo(base,altura):
    area=base*altura*(1/2)
    return area
    
def area_rectangulo(base,altura):
    area=base*altura
    return area

def area_rombo(diagonal1,diagonal2):
    area=diagonal1*diagonal2*(1/2)
    return area

def area_circulo(radio):
    pi=3.14159265358979323846
    area=(radio**2)*pi
    return area

