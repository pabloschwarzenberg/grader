from math import pi
def area_triangulo(base,altura):
    areat= (base*altura)/2
    return areat
def area_rectangulo(base,altura):
    areat=base*altura
    return areat
def area_rombo(diagonal1,diagonal2):
    areat= (diagonal1*diagonal2)/2
    return areat
def area_circulo(radio):
    areat= pi*radio**2
    return areat


           