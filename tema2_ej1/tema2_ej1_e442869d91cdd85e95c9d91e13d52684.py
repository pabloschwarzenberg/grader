import math

def area_triangulo(base,altura):
    t=base*altura/2
    return t
    pass

def area_rectangulo(base,altura):
    r=base*altura
    return r
    pass

def area_rombo(diagonal1,diagonal2):
    ro=diagonal1*diagonal2/2
    return ro
    pass

def area_circulo(radio):
    c=math.pi*float(radio**2)
    return c
    pass

