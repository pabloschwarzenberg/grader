def area_triangulo(base,altura):
    at=(base*altura)/2
    return at

def area_rectangulo(base,altura):
    ar= base*altura
    return ar

def area_rombo(diagonal1,diagonal2):
    aro= (diagonal1*diagonal2)/2
    return aro

def area_circulo(radio):
    ac=3.1415926535897932 *(radio)**2
    return ac

