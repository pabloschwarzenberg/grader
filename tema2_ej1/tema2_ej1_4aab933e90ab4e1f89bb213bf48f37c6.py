def area_triangulo(base,altura):
    base=(base*altura)/2
    return base

def area_rectangulo(base,altura):
    base=base*altura
    return base

def area_rombo(diagonal1,diagonal2):
    diagonal1=diagonal1*diagonal2/2
    return diagonal1
def area_circulo(radio):
    radio=(radio**2)*3.1415926535897932
    return radio
