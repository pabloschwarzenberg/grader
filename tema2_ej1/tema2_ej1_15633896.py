from math import pi

def area_triangulo(base,altura):
    a=altura
    b=base
    c=(a*b)/2
    return c

def area_rectangulo(base,altura):
    a=altura
    b=base
    c=a*b
    return c

def area_rombo(diagonal1,diagonal2):
    d1=diagonal1
    d2=diagonal2
    e=(d1*d2)/2
    return e
    

def area_circulo(radio):
    r=radio
    a=pi*(r**2)
    return a

    

           