from math import pi
def area_triangulo(base,altura):
    multi=base*altura
    division=multi/2
    return division
def area_rectangulo(base,altura):
    multiplicacion=base*altura
    return multiplicacion
def area_rombo(diagonal1,diagonal2):
    p=diagonal1*diagonal2
    m=p/2
    return m
def area_circulo(radio):
    digito=pi*radio**2
    return digito
           