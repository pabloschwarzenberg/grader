import math

def area_triangulo(base,altura):
    t = base*altura/2
    return t
    pass

def area_rectangulo(base,altura):
    t = base*altura
    return t
    pass

def area_rombo(diagonal1,diagonal2):
    t = diagonal1*diagonal2/2
    return t
    pass

def area_circulo(radio):
    t = math.pi*radio**2
    return t
    pass           