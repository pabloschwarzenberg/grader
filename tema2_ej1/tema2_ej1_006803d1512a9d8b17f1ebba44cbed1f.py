from math import pi

def area_rectangulo(base,altura):
  area=base*altura
  return area

def area_triangulo(base,altura):
  area1=(base*altura)/2
  return area1

def area_rombo(diagonal1,diagonal2):
  area2=(diagonal1*diagonal2)/2
  return area2

def area_circulo(radio):
  area3=pi*radio**2
  return area3