def area_triangulo(base,altura):
    AT = (base * altura)/2
    return AT

def area_rectangulo(base,altura):
    ARE = base * altura
    return ARE

def area_rombo(diagonal1,diagonal2):
    AR = (diagonal1 * diagonal2)/2
    return AR
def area_circulo(radio):
    pi = 3.1415926535897932
    AC = pi * radio ** 2
    return AC

