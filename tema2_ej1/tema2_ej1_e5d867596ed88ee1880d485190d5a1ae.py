def area_triangulo(base,altura):
    area_tri=base*altura/2
    return(area_tri)

def area_rectangulo(base,altura):
    area_rec=base*altura
    return(area_rec)
    
def area_rombo(x_1,x_2):
    area_rom= x_1*x_2/2
    return(area_rom)

import math
def area_circulo(radio):
    area_cir=math.pi*radio**2
    return(area_cir)