def area_triangulo(b,h):
    AT = b * h
    ATR = AT/2
    return ATR
############################
def area_rectangulo(x,y):
    AR = x * y
    return AR
############################
def area_rombo(d1,d2):
    AR = d1 * d2
    ARE = AR/2
    return ARE
############################
def area_circulo(r):
    PI = 3.1415926535897932
    AC = r**2
    ACI = AC*PI
    return ACI
    