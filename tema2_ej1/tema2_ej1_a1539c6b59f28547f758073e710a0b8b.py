def area_triangulo(base,altura):
  AreaTri= (base*altura)/2
  return AreaTri

def area_rectangulo(base,altura):
  AreaRec= base*altura
  return AreaRec

def area_rombo(diagonal1,diagonal2):
  AreaRom= (diagonal1*diagonal2)/2
  return AreaRom

def area_circulo(radio):
  pi= 3.1415926535897932 
  AreaCir= pi*radio**2 
  return AreaCir


           