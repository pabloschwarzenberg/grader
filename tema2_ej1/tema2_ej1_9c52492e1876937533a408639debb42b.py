import math
def area_triangulo(base,altura):
    salida=(base*altura)/2
    return salida

def area_rectangulo(base,altura):
    salida=base*altura  
    return(salida)

def area_rombo(diagonal1,diagonal2):
    salida=(diagonal1*diagonal2)/2
    return(salida)
    

def area_circulo(radio):
    salida= (math.pi)*(radio**2)

    return(salida)

