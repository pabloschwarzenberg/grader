def area_triangulo(base,altura):
    r1=base*altura/2
    return r1
def area_rectangulo(largo,altura2):
    r2=largo*altura2
    return r2
def area_rombo(diagonal,diagonal2):
    r3=(diagonal*diagonal2)/2
    return r3
def area_circulo(radio):
    import math
    r4=math.pi*(radio**2)
    return r4
           