def area_triangulo(b,h):
    AreaTriangulo = (b * h)/2
    return AreaTriangulo

def area_rectangulo(b,h):
    AreaRectangulo = b * h
    return AreaRectangulo

def area_rombo(d_1,d_2):
    AreaRombo = (d_1 * d_2)/2
    return AreaRombo

def area_circulo(r):
    pi = 3.1415926535897932
    AreaCirculo = pi * r**2
    return AreaCirculo
           