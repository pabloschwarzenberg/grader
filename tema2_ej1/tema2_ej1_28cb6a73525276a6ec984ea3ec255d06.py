import math
def area_triangulo(base,altura):
  res=(base*altura)/2
  return res


def area_rectangulo(lado1,lado2):
  res2= (lado1*lado2)
  return res2


def area_rombo(diagonalx,diagonaly):
  res3= (diagonalx*diagonaly)/2
  return res3


def area_circulo(r):
  res4= (math.pi*(r**2))
  return res4
          