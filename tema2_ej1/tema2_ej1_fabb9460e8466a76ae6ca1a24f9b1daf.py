import math
def area_triangulo(base,altura):
    Meow=(base*altura)/2
    return Meow

def area_rectangulo(base,altura):
    Meow=base*altura
    return Meow

def area_rombo(diagonal1,diagonal2):
    Meow=(diagonal1*diagonal2)/2
    return Meow

def area_circulo(radio):
    Meow=(radio*radio)*math.pi
    return Meow