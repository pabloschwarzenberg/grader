#calculadora geometrica
def area_triangulo(b,h):
    AreaT = (b * h)/2
    return AreaT

def area_rectangulo(b,h):
    AreaR = b * h
    return AreaR

def area_rombo(d1,d2):
    AreaRom = (d1 * d2)/2
    return AreaRom

def area_circulo(r):
    pi = 3.1415926535897932
    AreaC = pi * r**2
    return AreaC    
           