pi = 3.1416
def areaCirculo(radio):
        area = pi*radio**2
        return area

def areaRectangulo(largo, ancho):
        area = largo*ancho
        return area

def areaRombo(diagonal1, diagonal2):
        area = (diagonal1*diagonal2)/2
        return area

def areaTriangulo(base, altura):
        round.area = base*altura/2
        return round.area