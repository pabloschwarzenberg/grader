def area_triangulo(base,altura):
  resultado = (base*altura)/2
  print("Resultado: ")
  print(resultado)
def area_rectangulo(base,altura):
  resultado = base*altura
  print("Resultado: ")
  print(resultado)
def area_rombo(diagonal1,diagonal2):
  resultado = (diagonal1*diagonal2)/2
  print("Resultado: ")
  print(resultado)
