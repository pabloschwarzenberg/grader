def area_rectangulo(base,altura):
  arear=base*altura
  return(arear)
def area_triangulo(base,altura):
  areat=(base*altura)/2
  return(areat)
def area_rombo(diagonal1, diagonal2):
  arearo=(diagonal1*diagonal2)/2
  return(arearo)
def area_circulo(radio):
  import math
  areac=(radio**2)*math.pi
  return(areac)
           