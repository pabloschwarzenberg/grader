import math

def area_triangulo(base,altura):
  areaDeTriangulo = (base*altura)/2
  return areaDeTriangulo
   

def area_rectangulo(base,altura):
  areaderectangulo = base*altura
  return areaderectangulo
    

def area_rombo(diagonal1,diagonal2):
  areaRombo = (diagonal1*diagonal2)/2
  return areaRombo
  

def area_circulo(radio):
  areadecirculo = 3.141592653589793*(radio**2)
  return areadecirculo
  
  
