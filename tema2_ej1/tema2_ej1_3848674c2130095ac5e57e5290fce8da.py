def area_triangulo(base,altura):
    area_T=base*altura/2
    return area_T

def area_rectangulo(base,altura):
    area_R=base*altura
    return area_R

def area_rombo(diagonal1,diagonal2):
    area_Ro=diagonal1*diagonal2/2
    return area_Ro

def area_circulo(radio):
    area_C=3.141592653589793*(radio**2)
    return area_C
           