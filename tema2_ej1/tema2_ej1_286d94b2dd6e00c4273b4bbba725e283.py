import math
def area_triangulo(base,altura):
  x=base*altura/2
  return x
  pass
def area_rectangulo(base,altura):
  x=base*altura
  return x
  pass

def area_rombo(diagonal1,diagonal2):
  x=diagonal1*diagonal2/2
  return x
  pass

def area_circulo(radio):
  a=math.pi
  x=a*radio*radio
  return x
  pass
  