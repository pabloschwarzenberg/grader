def area_triangulo(base,altura):
 calculando = (base * altura)/2
 return calculando

def area_rectangulo(base,altura):
 sacando = base* altura
 return sacando

def area_rombo(diagonal1,diagonal2):
 area = (diagonal1 * diagonal2)/2
 return area

def area_circulo(radio):
 mostrando = 3.141592653589793238462* (radio * radio)
 return mostrando

