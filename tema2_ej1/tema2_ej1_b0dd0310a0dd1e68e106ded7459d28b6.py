def area_triangulo(base,altura):
    formula=(1/2)*base*altura
    return formula
def area_rectangulo(base,altura):
    formula=base*altura
    return formula
def area_rombo(diagonal1,diagonal2):
    formula=diagonal1*diagonal2*(1/2)
    return formula
def area_circulo(radio):
    formula=3.1415926535897932384*(radio**2)
    return formula
