def area_triangulo(base,altura):
  casiarea=base*altura
  area=casiarea/2
  return area  

def area_rectangulo(base,altura):
  casiarea=base*altura
  return casiarea

def area_rombo(diagonal1,diagonal2):
  casiarea=diagonal1*diagonal2
  area=casiarea/2
  return area
  
