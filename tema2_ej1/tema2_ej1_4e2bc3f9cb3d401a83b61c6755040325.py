def area_triangulo(base,altura):
    AreaT = (base * altura)/2
    return AreaT

def area_rectangulo(base,altura):
    AreaR = base * altura
    return AreaR

def area_circulo(radio_circulo):
    N_pi = 3.1415926535897932
    AreaC = N_pi * radio_circulo**2
    return AreaC

def area_rombo(Linea,Linea2):
    AreaR = (Linea * Linea2)/2
    return AreaR