def area_triagulo(base,altura):  
    area=(base*altura)/2
    return(area)
def area_rectangulo(base,altura):
    arear=base*altura
    return(arear)

def area_rombo(diagonal1,diagonal2):
    arearo=(diagonal1*diagonal2)/2
    return(arearo)
from math import pi
def area_circulo(radio):
    areac=pi*radio**2
    return(areac)



