from math import pi
def area_triangulo(b,a):
    area = ((b*a))/2
    return area
def area_rectangulo(b,a):
    area = b*a
    return area
def area_rombo(d1,d2):
    area = ((d1 * d2))/2
    return area
def area_circulo(r):
    area = pi*(r**2)
    return area

