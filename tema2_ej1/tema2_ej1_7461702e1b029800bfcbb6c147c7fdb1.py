def area_triangulo(base,altura):
    m=base*altura
    r=m/2
    return r
def area_rectangulo(base,altura):
    m=base*altura
    return m
def area_rombo(diagonal1,diagonal2):
    m=diagonal1*diagonal2
    r=m/2
    return r
def area_circulo(radio):
    pi=3.14159265358979323846264
    c=radio**2
    m=pi*c
    return m
           