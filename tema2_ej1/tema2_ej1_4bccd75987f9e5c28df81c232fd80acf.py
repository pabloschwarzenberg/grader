from math import pi
def area_triangulo(base,altura):
    area= (base * altura)/2
    return area

def area_rectangulo(base,altura):
    arearec = base* altura
    return arearec
def area_rombo(diagonal1,diagonal2):
    arearomb = (diagonal1 * diagonal2)/2
    return arearomb

def area_circulo(radio):
    areaci= pi * radio**2
    return areaci


           