def area_triangulo(base,altura):
    Area = (base*altura)/2
    return Area
    pass

def area_rectangulo(base,altura):
    Area = base*altura
    return Area
    pass

def area_rombo(diagonal1,diagonal2):
    Area = (diagonal1*diagonal2)/2
    return Area
    pass

def area_circulo(radio):
    import math
    Area = math.pi*(radio**2)
    return Area
    pass


           