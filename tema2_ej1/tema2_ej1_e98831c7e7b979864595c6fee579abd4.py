import math
def area_triangulo(base,altura):
    areat=base*altura/2
    return areat

def area_rectangulo(base,altura):
    area_rect=base*altura
    return area_rect

def area_rombo(diagonal1,diagonal2):
    area_rom=diagonal1*diagonal2/2
    return area_rom

def area_circulo(radio):
    areac=math.pi*(radio**2)
    return areac


           