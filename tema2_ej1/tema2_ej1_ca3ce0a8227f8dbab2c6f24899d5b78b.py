import math
def area_triangulo(b,h):
    area=b*h/2
    return area
def area_rectangulo(b,h):
    area=b*h
    return area
def area_rombo(diagonal1,diagonal2):
    area = (diagonal1*diagonal2)/2
    return area
def area_circulo(R):
    area=math.pi*R**2
    return area
