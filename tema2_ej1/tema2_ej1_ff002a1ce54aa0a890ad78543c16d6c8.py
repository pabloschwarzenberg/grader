import math
def area_triangulo(base,altura):
   ma=(base*altura)/2
   return(ma)
def area_rectangulo(lado1, lado2):
   rc=(lado1*lado2)
   return(rc)
def area_rombo(diagonal1,diagonal2):
   ce=(diagonal1*diagonal2)/2
   return(ce)
def area_circulo(radio):
    lo=(math.pi*radio**2)
    return(lo)
   