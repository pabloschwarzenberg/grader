import math
pi= math.pi
def area_triangulo(base,altura):
    a = (base * altura)/2
    return (a)
    pass

def area_rectangulo(base,altura):
    a = base * altura
    return(a)
    pass


def area_rombo(diagonal1,diagonal2):
    a = (diagonal1 * diagonal2)/2
    return(a)
    pass

def area_circulo(radio):
    a = pi*(radio**2)
    return(a)
    pass
