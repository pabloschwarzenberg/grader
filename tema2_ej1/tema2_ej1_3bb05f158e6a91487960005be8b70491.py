import math

def area_triangulo(b,altura):
    area = (b * altura)/2
    return area
def area_rectangulo(b,altura):
    area = (b * altura)
    return area
def area_rombo(d1,d2):
    area = (d1 * d2)/2
    return area
def area_circulo(radio):
    area = math.pi * (radio ** 2)
    return area
         