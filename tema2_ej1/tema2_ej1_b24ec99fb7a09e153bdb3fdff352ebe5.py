from math import pi

def areaCirculo(radio):
    area = pi*radio**2
    return area

def areaRectangulo(largo, ancho):
    area = largo*ancho
    return area

def areaRombo(lado):
    area = lado*lado
    return area

def areaTriangulo(base, altura):
    area = base*altura/2
    return area
        

