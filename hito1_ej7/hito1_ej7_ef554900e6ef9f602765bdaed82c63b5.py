#Zodiaco
# Ingreso de variables
dia=int(input("Ingrese dia de cumpleaños: "))
mes=int(input("ingrese mes de cumpleaños: "))
# Aries (21 de Marzo - 20 de Abril)
if((21<=dia<=31) and (mes==3)) or ((1<=dia<=20) and (mes==4)):
    print("Su signo es: Aries")
# Tauro (21 de Abril - 21 de Mayo)
if((21<=dia<=30) and (mes==4)) or ((1<=dia<=21) and (mes==5)):
    print("Su signo es: Tauro")
# Geminis (22 de Mayo - 21 de Junio)
if((22<=dia<=31) and (mes==5)) or ((1<=dia<=21) and (mes==6)):
    print("Su signo es: Geminis")
# Cancer (22 de Junio - 22 de Julio)
if((22<=dia<=30) and (mes==6)) or ((1<=dia<=22) and (mes==7)):
    print("Su signo es: Cancer")
# Leo (23 de Julio - 22 de Agosto)
if((23<=dia<=31) and (mes==7)) or ((1<=dia<=22) and (mes==8)):
    print("Su signo es: Leo")
# Virgo (23 de Agosto - 23 de Septiembre)
if((23<=dia<=31) and (mes==8)) or ((1<=dia<=23) and (mes==9)):
    print("Su signo es: Virgo")
# Libra (24 de Septiembre - 23 de Octubre)
if((24<=dia<=30) and (mes==9)) or ((1<=dia<=23) and (mes==10)):
    print("Su signo es: Libra")
# Escorpion (24 de Octubre - 22 de Noviembre)
if((24<=dia<=31) and (mes==10)) or ((1<=dia<=22) and (mes==11)):
    print("Su signo es: Escorpion")
# Sagitario (23 de Noviembre - 21 de Diciembre)
if((23<=dia<=30) and (mes==11)) or ((1<=dia<=21) and (mes==12)):
    print("Su signo es: Sagitario")
# Capricornio (22 de Diciembre - 20 de Enero)
if((22<=dia<=31) and (mes==12)) or ((1<=dia<=20) and (mes==1)):
    print("Su signo es: Capricornio")
# Aquario (21 de Enero - 19 de Febrero)
if((21<=dia<=31) and (mes==1)) or ((1<=dia<=19) and (mes==2)):
    print("Su signo es: Aquario")
# Piscis (20 de Febrero - 20 de Marzo)
if((21<=dia<=29) and (mes==2)) or ((1<=dia<=20) and (mes==3)):
    print("Su signo es: Piscis")