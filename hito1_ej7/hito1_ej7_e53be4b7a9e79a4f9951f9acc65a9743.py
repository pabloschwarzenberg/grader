if (dia >= 23 and mes == 10) or (dia < 22 and mes == 11):
    print('ESCORPION')
#SAGITARIOS: 22 Noviembre – 22 Diciembre
if (dia >= 22 and mes == 11) or (dia < 22 and mes == 12):
    print('SAGITARIO') 
#CAPRICORNIO: 22 Diciembre – 20 Junio
if (dia >= 22 and mes == 12) or (dia < 20 and mes == 6):
    print('CAPRICORNIO')
#ACUARIO: 20 Junio – 19 Febrero
