#Zodiaco

dia= int(input('Ingresa tu dia de cumpleaños: '))

mes= int(input('Ingresa tu mes de cumpleaños: '))

if (21<=dia<=31 and mes==3) or (1<=dia<=19 and mes==4):
    print('Tu signo zodiacal es Aries')

if (20<=dia<=30 and mes==4) or (1<=dia<=20 and mes==5):
    print('Tu signo zodiacal es Tauro')

if (21<=dia<=31 and mes==5) or (1<=dia<=21 and mes==6):
    print('Tu signo zodiacal es Géminis')

if (22<=dia<=30 and mes==6) or (1<=dia<=22 and mes==7):
    print('Tu signo zodiacal es Cáncer')

if (23<=dia<=31 and mes==7) or (1<=dia<=23 and mes==8):
    print('Tu signo zodiacal es Leo')

if (24<=dia<=31 and mes==8) or (1<=dia<=22 and mes==9):
    print('Tu signo zodiacal es Virgo')

if (23<=dia<=30 and mes==9) or (1<=dia<=23 and mes==10):
    print('Tu signo zodiacal es Libra')

if (24<=dia<=31 and mes==10) or (1<=dia<=22 and mes==11):
    print('Tu signo zodiacal es Escorpión')

if (23<=dia<=30 and mes==11) or (1<=dia<=22 and mes==12):
    print('Tu signo zodiacal es Sagitario')

if (23<=dia<=31 and mes==12) or (1<=dia<=20 and mes==1):
    print('Tu signo zodiacal es Capricornio')

if (21<=dia<=31 and mes==1) or (1<=dia<=18 and mes==2):
    print('Tu signo zodiacal es Acuario')

if (19<=dia<=28 and mes==2) or (1<=dia<=20 and mes==3):
    print('Tu signo zodiacal es Piscis')
      