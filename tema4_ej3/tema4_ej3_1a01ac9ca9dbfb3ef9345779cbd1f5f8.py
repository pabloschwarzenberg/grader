def jerigonzo(string):
    stringge=""
    for i in string:
        if i==("a"):
            stringge+="apa"
        elif i==("e"):
            stringge+="epe"
        elif i==("i"):
            stringge+="ipi"
        elif i==("o"):
            stringge+="opo"
        elif i==("u"):
            stringge+="upu"
        else:
            stringge+=i

    return stringge
 

