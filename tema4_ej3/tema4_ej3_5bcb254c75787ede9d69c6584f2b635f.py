def jerigonzo(string):
    lista=string.split("a")
    a="apa".join(lista)
    lista1=a.split("e")
    b="epe".join(lista1)
    lista2=b.split("i")
    c="ipi".join(lista2)
    lista3=c.split("o")
    d="opo".join(lista3)
    lista4=d.split("u")
    e="upu".join(lista4)
    return e

