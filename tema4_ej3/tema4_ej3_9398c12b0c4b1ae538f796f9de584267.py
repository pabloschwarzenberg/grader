def jerigonzo(string):
    string=string.lower()
    string=string.replace("a","apa",len(string))
    string=string.replace("e","epe",len(string))
    string=string.replace("i","ipi",len(string))
    string=string.replace("o","opo",len(string))
    string=string.replace("u","upu",len(string))
    return string