#Sistema de Ecuaciones
a = float(input("primer numero: "))
b = float(input("segundo numero: "))
c = float(input("tercer numero: "))
d = float(input("cuarto numero: "))
e = float(input("quinto numero: "))
f = float(input("sexto numero: "))
z = a*e-b*d
x = (e*c-b*f)/z
y = (a*f-c*d)/z
print("x=",x)
print("y=",y)
