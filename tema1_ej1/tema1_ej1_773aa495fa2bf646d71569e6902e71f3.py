#Suma de los N primeros números#La suma de los primeros n numeros naturales está dada por n(n + 1)/2. 
#Crea un programa que reciba como parámetro un número N y luego imprima la suma de los N primeros números naturales

n = int(input("ingrese un número entero: "))
suma = n * (n + 1) / 2
print (suma)