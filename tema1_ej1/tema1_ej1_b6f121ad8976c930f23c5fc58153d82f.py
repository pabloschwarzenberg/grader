#Suma de los N primeros números
# Se solicita al usuario que ingrese un número N
N = int(input("Ingrese un número entero positivo: "))

# Se calcula la suma de los N primeros números naturales utilizando la fórmula
suma = N * (N + 1) // 2

# Se imprime la suma de los N primeros números naturales
print("La suma de los", N, "primeros números naturales es:", suma)
     