def rot13(palabra):
    lista=list(palabra)
    letras=["a","b","c","d","e","f","g","h","i","j","k","l","m","n","o","p","q","r","s","t","u","v","w","x","y","z"]
    cambio=["n","o","p","q","r","s","t","u","v","w","x","y","z","a","b","c","d","e","f","g","h","i","j","k","l","m"]
    nuevo=""
    for i in palabra:
        nuevo=nuevo+cambio[letras.index(i)]
    return nuevo


