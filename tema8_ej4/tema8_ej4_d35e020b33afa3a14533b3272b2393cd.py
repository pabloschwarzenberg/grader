def rot13(palabra):
    lista=list(palabra)
    abc=["a","b","c","d","e","f","g","h","i","j","k","l","m","n","o","p","q","r","s","t","u","v","w","x","y","z"]
    change=["n","o","p","q","r","s","t","u","v","w","x","y","z","a","b","c","d","e","f","g","h","i","j","k","l","m"]
    cifrado= ""
    for stuff in lista:
      cifrado+=change[abc.index(stuff)]
    return cifrado