def rot13(palabra):
  palabrin=""
  abc=["a","b","c","d","e","f","g","h","i","j","k","l","m","n","o","p","q","r","s","t","u","v","w","x","y","z"]
  rot13=["n","o","p","q","r","s","t","u","v","w","x","y","z","a","b","c","d","e","f","g","h","i","j","k","l","m",]
  
  for i in range(len(palabra)):
    m=abc.index(palabra[i])
    palabrin=palabrin+rot13[m]    
  return palabrin
  
