def rot13(palabra):
    ab_l = ["a", "b", "c", "d", "e", "f", "g", "h", "i", "j", "k", "l", "m", "n", "o", "p", "q", "r", "s", "t", "u", "v", "w", "x", "y", "z", "a", "b", "c", "d", "e", "f", "g", "h", "i", "j", "k", "l", "m", "n", "o", "p", "q", "r", "s", "t", "u", "v", "x", "y", "z"]
    ab_s = "abcdefghijklmnopqrstuvwxyz"
    string = ""
    for letras in palabra:     
            a = ab_s.find(letras)
            string = string + ab_l[a + 13]
    return string
            
    
