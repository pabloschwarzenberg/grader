    def __init__(self,nombre,email):
        self.nombre=nombre
        self.email=email
        self.password=""

    def cambiar_password(self,password):
        abc=['A', 'B', 'C', 'D', 'E', 'F', 'G', 'H', 'I', 'J', 'K', 'L', 'M', 'N', 'Ã‘', 'O', 'P', 'Q', 'R', 'S', 'T', 'U', 'V', 'W', 'X', 'Y', 'Z']
        chr_sp=["_"]
        contador=0
        otro=0
        for i in password:
            if i in abc:
                contador+=1
        for k in password:
            if k in chr_sp:
                otro+=1
        if len(password)<8 or len(password)>16 or contador==0 and otro==0:
            return False
        else:
            self.password=password
            return True      

    def login(self,password):
        if password==self.password:
            return True
        else:
            return False

